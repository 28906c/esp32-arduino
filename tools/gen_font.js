// Use --symbols to pass actual characters via Node.js (no encoding issues)
// lv_font_conv will correctly split into multiple cmaps for different Unicode blocks
const args = [
    '--font', 'C:\\Windows\\Fonts\\simhei.ttf',
    '--size', '24',
    '--bpp', '4',
    '--format', 'lvgl',
    '-o', path.join(__dirname, '..', 'main', 'src', 'ui', 'lv_font_chinese_24.c'),
    '--lv-font-name', 'lv_font_chinese_24',
    '--lv-include', 'lvgl.h',
    '--no-compress',
    '--lv-fallback', 'lv_font_ascii_24',
    '--symbols', chars_dedup
];

console.log('Running lv_font_conv...');
const r = spawnSync(process.execPath, ['C:\\nvm4w\\nodejs\\node_modules\\lv_font_conv\\lv_font_conv.js'].concat(args), { stdio: 'inherit', cwd: __dirname });
if (r.error) { console.error('spawn error:', r.error); process.exit(1); }
if (r.status !== 0) { process.exit(r.status); }

